package com.property.bean;

import java.util.Date;

public class Block {
	private int blockId;
	private String blockName;
	private String manager;
	private Date buildTime;
	private int structArea;
	private int number;
	private String address;
	private int greenArea;
	private int roadArea;
	private int parkArea;
	private String tel;
	private String detail;
	public int getBlockId() {
		return blockId;
	}
	public void setBlockId(int blockId) {
		this.blockId = blockId;
	}
	public String getBlockName() {
		return blockName;
	}
	public void setBlockName(String blockName) {
		this.blockName = blockName;
	}
	public String getManager() {
		return manager;
	}
	public void setManager(String manager) {
		this.manager = manager;
	}
	public Date getBuildTime() {
		return buildTime;
	}
	public void setBuildTime(Date buildTime) {
		this.buildTime = buildTime;
	}
	public int getStructArea() {
		return structArea;
	}
	public void setStructArea(int structArea) {
		this.structArea = structArea;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public int getGreenArea() {
		return greenArea;
	}
	public void setGreenArea(int greenArea) {
		this.greenArea = greenArea;
	}
	public int getRoadArea() {
		return roadArea;
	}
	public void setRoadArea(int roadArea) {
		this.roadArea = roadArea;
	}
	public int getParkArea() {
		return parkArea;
	}
	public void setParkArea(int parkArea) {
		this.parkArea = parkArea;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	
	
}
