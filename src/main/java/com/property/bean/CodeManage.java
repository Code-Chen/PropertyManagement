package com.property.bean;

public class CodeManage {
	private int codeManageId;
	private int type;
	private int code;
	private String name;
	private String decorationStandard;

	public int getCodeManageId() {
		return codeManageId;
	}

	public void setCodeManageId(int codeManageId) {
		this.codeManageId = codeManageId;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDecorationStandard() {
		return decorationStandard;
	}

	public void setDecorationStandard(String decorationStandard) {
		this.decorationStandard = decorationStandard;
	}
}
