package com.property.bean;

public class House {
	private int houseId;
	private int bulid;
	private int cell;
	private String name;
	private int stair;
	private int direction;
	private int houseType;
	private int funType;
	private int decorate;
	private int buildArea;
	private int usedArea;
	private int isSold;

	public int getHouseId() {
		return houseId;
	}

	public void setHouseId(int houseId) {
		this.houseId = houseId;
	}

	public int getBulid() {
		return bulid;
	}

	public void setBulid(int bulid) {
		this.bulid = bulid;
	}

	public int getCell() {
		return cell;
	}

	public void setCell(int cell) {
		this.cell = cell;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getStair() {
		return stair;
	}

	public void setStair(int stair) {
		this.stair = stair;
	}

	public int getDirection() {
		return direction;
	}

	public void setDirection(int direction) {
		this.direction = direction;
	}

	public int getHouseType() {
		return houseType;
	}

	public void setHouseType(int houseType) {
		this.houseType = houseType;
	}

	public int getFunType() {
		return funType;
	}

	public void setFunType(int funType) {
		this.funType = funType;
	}

	public int getDecorate() {
		return decorate;
	}

	public void setDecorate(int decorate) {
		this.decorate = decorate;
	}

	public int getBuildArea() {
		return buildArea;
	}

	public void setBuildArea(int buildArea) {
		this.buildArea = buildArea;
	}

	public int getUsedArea() {
		return usedArea;
	}

	public void setUsedArea(int usedArea) {
		this.usedArea = usedArea;
	}

	public int getIsSold() {
		return isSold;
	}

	public void setIsSold(int isSold) {
		this.isSold = isSold;
	}

}
